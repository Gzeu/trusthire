  /**
   * Calculate workflow confidence based on results
   */
  private calculateWorkflowConfidence(workflow: HuntingWorkflow): number {
    if (workflow.queries.length === 0) return 0;
    
    const successfulQueries = workflow.queries.filter(q => q.status === 'completed');
    const querySuccessRate = successfulQueries.length / workflow.queries.length;
    
    const findingCount = workflow.findings.length;
    const findingScore = Math.min(findingCount / 10, 1); // Normalize to 0-1
    
    return (querySuccessRate * 0.6 + findingScore * 0.4);
  }

  /**
   * Calculate next execution time
   */
  private calculateNextExecution(schedule: HuntingWorkflow['schedule']): Date {
    const now = new Date();
    const interval = schedule.interval;
    
    switch (schedule.frequency) {
      case 'hourly':
        return new Date(now.getTime() + interval * 60 * 60 * 1000);
      case 'daily':
        return new Date(now.getTime() + interval * 24 * 60 * 60 * 1000);
      case 'weekly':
        return new Date(now.getTime() + interval * 7 * 24 * 60 * 60 * 1000);
      case 'monthly':
        return new Date(now.getTime() + interval * 30 * 24 * 60 * 60 * 1000);
      default:
        return now;
    }
  }

  /**
   * Group array items by property
   */
  private groupBy<T>(items: T[], property: keyof T): Record<string, number> {
    return items.reduce((groups, item) => {
      const key = String(item[property]);
      groups[key] = (groups[key] || 0) + 1;
      return groups;
    }, {} as Record<string, number>);
  }

  /**
   * Calculate recent trend for findings
   */
  private calculateRecentTrend(findings: HuntFinding[]): number {
    const now = new Date();
    const weekAgo = new Date(now.getTime() - 7 * 24 * 60 * 60 * 1000);
    
    const recentFindings = findings.filter(f => f.createdAt >= weekAgo);
    const previousWeek = findings.filter(f => {
      const twoWeeksAgo = new Date(now.getTime() - 14 * 24 * 60 * 60 * 1000);
      return f.createdAt >= twoWeeksAgo && f.createdAt < weekAgo;
    });
    
    if (previousWeek.length === 0) return recentFindings.length > 0 ? 1 : 0;
    
    return (recentFindings.length - previousWeek.length) / previousWeek.length;
  }

