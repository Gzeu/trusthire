  /**
   * Filter and prioritize hypotheses
   */
  private filterAndPrioritizeHypotheses(hypotheses: HuntingHypothesis[]): HuntingHypothesis[] {
    const threshold = this.config.hypothesisGeneration.confidenceThreshold;
    const maxActive = this.config.hypothesisGeneration.maxActiveHypotheses;
    
    // Filter by confidence threshold
    const filtered = hypotheses.filter(h => h.confidence >= threshold);
    
    // Sort by priority and confidence
    filtered.sort((a, b) => {
      const priorityOrder = { critical: 4, high: 3, medium: 2, low: 1 };
      const priorityDiff = priorityOrder[b.priority] - priorityOrder[a.priority];
      return priorityDiff !== 0 ? priorityDiff : b.confidence - a.confidence;
    });
    
    // Limit to maximum active hypotheses
    return filtered.slice(0, maxActive);
  }

  /**
   * Get workflows ready for execution
   */
  private getWorkflowsReadyForExecution(): HuntingWorkflow[] {
    const now = new Date();
    
    return Array.from(this.workflows.values())
      .filter(w => {
        // Check if workflow is pending or scheduled for execution
        if (w.status !== 'pending' && w.status !== 'completed') {
          return false;
        }
        
        // Check if it's time for execution
        if (w.nextExecution && w.nextExecution > now) {
          return false;
        }
        
        return true;
      });
  }

  /**
   * Execute a single hunting workflow
   */
  private async executeWorkflow(workflow: HuntingWorkflow): Promise<HuntingWorkflow> {
    console.log(`Executing workflow: ${workflow.name}`);
    
    workflow.status = 'running';
    workflow.startedAt = new Date();
    this.emit('workflow:started', workflow);
    
    try {
      // Execute all queries in the workflow
      for (const query of workflow.huntQueries) {
        const execution = await this.executeQuery(query, workflow.id);
        workflow.queries.push(execution);
        
        // Analyze results and generate findings
        if (execution.status === 'completed' && execution.resultCount > 0) {
          const findings = await this.analyzeResults(execution, workflow.id);
          workflow.findings.push(...findings);
        }
      }
      
      // Calculate overall confidence
      workflow.confidence = this.calculateWorkflowConfidence(workflow);
      
      // Schedule next execution if needed
      if (workflow.schedule.frequency !== 'once') {
        workflow.nextExecution = this.calculateNextExecution(workflow.schedule);
      }
      
      workflow.status = 'completed';
      workflow.completedAt = new Date();
      
      this.emit('workflow:completed', workflow);
      console.log(`Workflow completed: ${workflow.name}`);
      
    } catch (error) {
      workflow.status = 'failed';
      this.emit('workflow:failed', workflow);
      throw error;
    }
    
    return workflow;
  }

